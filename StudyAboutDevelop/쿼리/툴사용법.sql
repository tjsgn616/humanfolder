- CTRL + ENTER : 현재 쿼리 실행
- ALT + X : 현재 블록지정된 스크립트 실행
- CTRL SHIFT E : 현재 쿼리 실행계획 설명
- CTRL + SPACE : 자동완성
- CTRL + SHIFT + F : 자동 줄맞춤
- Ctrl shift x : 대문자로 변경
- Ctrl shift y : 소문자로 변경
- F4 : 테이블 정의
- 쉽게 쿼리작성하는법(컬럼가져오는법) : SELECT한 결과 테이블 우클릭 - SQL생성



TAB : 기록/격자 모드 전환
ALT + DELETE = 행삭제
ALT + INSERT = 새행추가
CTRL + ALT + INSERT = 현재 행 복사
ESC = 현재 셀/행의 변경사항 취소
CTRL + R = 변경작업 취소

ALT + ENTER = 선택한 요소의 속성 표시